def check_password():
    correct_password = "ariva"
    attempts = 3
    while attempts > 0:
        print(Fore.CYAN + "\n" + "═" * 50 + Style.RESET_ALL)
        animate_text("Erişim Kodu Gerekiyor: ", Fore.MAGENTA, 0.02)
        password = getpass("")
        if password == correct_password:
            animate_text("✓ Erişim Onaylandı", Fore.GREEN, 0.02)
            time.sleep(0.5)
            return True
        else:
            attempts -= 1
            animate_text(f"✗ Erişim Reddedildi | Kalan: {attempts}", Fore.RED, 0.02)
            time.sleep(1)
    animate_text("✗ Çok Fazla Hatalı Deneme | Kapatılıyor...", Fore.RED, 0.02)
    time.sleep(1)
    sys.exit()

