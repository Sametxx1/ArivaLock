    def check_single_site(self, site: str, url_template: str, username: str) -> None:
        url = url_template.format(quote(username))
        for attempt in range(self.max_retries):
            try:
                response = requests.get(
                    url,
                    headers=self.headers,
                    timeout=self.timeout
                )
                status = response.status_code
                if status == 200:
                    result = f"[✓] {site}: Bulundu! - {url}"
                elif status == 404:
                    result = f"[✗] {site}: Bulunamadı"
                elif status == 403:
                    result = f"[?] {site}: Erişim Engellendi (403) - {url}"
                elif status == 429:
                    result = f"[!] {site}: Çok Fazla İstek (429) - {url}"
                    time.sleep(2 ** attempt)
                    continue
                else:
                    result = f"[?] {site}: Durum Kodu {status} - {url}"
                break
            except requests.RequestException as e:
                if attempt == self.max_retries - 1:
                    result = f"[!] {site}: Hata - {str(e)}"
                else:
                    time.sleep(2 ** attempt)
                    continue
        self.results.put((site, result))
        self._save_to_db(username, site, result)

